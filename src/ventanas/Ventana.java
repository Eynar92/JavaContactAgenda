    private void botonGuardarContacoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarContacoActionPerformed
        Archivo archivo = new Archivo();
        archivo.crearArchivo();
        
        String nombre, correo, celular;
        // Obtenemos los datos de las cajas de texto
        nombre = cajaNombre.getText();
        correo = cajaCorreo.getText();
        celular = cajaCelular.getText();
        // Creamos un objeto persona
        Persona persona = new Persona(nombre, correo, celular);
        // Escribimos los datos de la persona en el archivo de .txt
        archivo.escribirTexto(persona);
        
        cajaNombre.setText("");
        cajaCorreo.setText("");
        cajaCelular.setText("");
    }//GEN-LAST:event_botonGuardarContacoActionPerformed

